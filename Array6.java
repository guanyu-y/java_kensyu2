class Array6{
  public static void main(String[] args){
    int [][] ary = {{1,2,3}, {10,20}, {100,200,300,400}};

    System.out.println("ary[0][0]=" + ary[0][0]);
    System.out.println("ary[0][2]=" + ary[0][2]);
    System.out.println("ary[1][0]=" + ary[1][0]);
    System.out.println("ary[2][3]=" + ary[2][3]);
  }
}
